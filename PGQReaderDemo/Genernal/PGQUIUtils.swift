import Foundation

//添加分割线

func SetUpViewLine(_ view:UIView, color:UIColor?) -> UIView {
    let viewLine = UIView()
    viewLine.backgroundColor = color != nil ? color : UIColor.red
    view.addSubview(viewLine)
    return viewLine
}






















































